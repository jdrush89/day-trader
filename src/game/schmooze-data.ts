// Pick N unique random compliments for a schmooze session
export function pickSchmoozeRounds(numRounds: number, seed?: number): { compliment: string; insults: string[] }[] {
  const rng = seed != null ? seededRandom(seed) : Math.random;
  const usedCompliments = new Set<number>();
  const usedInsults = new Set<number>();
  const rounds: { compliment: string; insults: string[] }[] = [];

  for (let i = 0; i < numRounds; i++) {
    // Pick a unique compliment
    let cIdx: number;
    do { cIdx = Math.floor(rng() * COMPLIMENTS.length); } while (usedCompliments.has(cIdx));
    usedCompliments.add(cIdx);

    // Pick 3 unique insults
    const roundInsults: string[] = [];
    for (let j = 0; j < 3; j++) {
      let iIdx: number;
      do { iIdx = Math.floor(rng() * INSULTS.length); } while (usedInsults.has(iIdx));
      usedInsults.add(iIdx);
      roundInsults.push(INSULTS[iIdx]);
    }

    rounds.push({ compliment: COMPLIMENTS[cIdx], insults: roundInsults });
  }

  return rounds;
}

// Shuffle options for display (returns indices mapping to original positions)
export function shuffleOptions(round: { compliment: string; insults: string[] }, seed?: number): { options: string[]; correctIndex: number } {
  const rng = seed != null ? seededRandom(seed) : Math.random;
  const options = [round.compliment, ...round.insults];
  // Fisher-Yates shuffle
  for (let i = options.length - 1; i > 0; i--) {
    const j = Math.floor(rng() * (i + 1));
    [options[i], options[j]] = [options[j], options[i]];
  }
  return { options, correctIndex: options.indexOf(round.compliment) };
}

function seededRandom(seed: number): () => number {
  let s = seed;
  return () => {
    s = (s * 1664525 + 1013904223) & 0xffffffff;
    return (s >>> 0) / 0xffffffff;
  };
}
